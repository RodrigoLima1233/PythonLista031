'''
Fazer um algorítimo que pergunte 3 números e apresente a média aritimética entre esses 3 números
'''

num1 = float(input("Qual é  seu primeira número?"))
num2 = float(input("Qual é  seu segunda número?"))
num3 = float(input("Qual é  seu terceira número?"))


media = (num1 + num2 + num3) / 3

print("Sua média é,", media)






